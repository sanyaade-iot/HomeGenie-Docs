                <h3>Instructions</h3>
                 
                <p>
                    First we need to download the HomeGenie SD card image for Banana Pi. We can grab it from the <a href="/download.php">Download</a> section.
                    <br />
		    Then we  will "flash" the just downloaded image file to the 4Gb SD card. See the article <a href="http://computers.tutsplus.com/articles/how-to-flash-an-sd-card-for-raspberry-pi--mac-53600" target="_blank">"How to flash an SD card"</a> for further informations about how to to this.
			
                </p>


<p></p>

<p>
After flashing the SD card, we are ready to assemble all parts (Banana Pi, SD card and enclosure).

<p>
Please note that HG SD card is based on a standard Raspbian image, so is also suggested to read the following article about securing your Pi SD card installation: <a href="http://mattwilcox.net/archives/setting-up-a-secure-home-web-server-with-raspberry-pi/" target="_blank">SETTING UP A (REASONABLY) SECURE HOME WEB-SERVER WITH RASPBERRY PI</a>
                    </span>
</p>

<p>
We can now plug <span style="font-family:'Lobster',sans-serif">Jeannie</span> HDMI output to a TV/Monitor or just power it up and use a remote client (eg. the HomeGenie Android app) to play with it.
To use a remote client, <span style="font-family:'Lobster',sans-serif">Jeannie</span> must first be connected to our local network using a network cable or an USB Wi-Fi dongle.
</p>

